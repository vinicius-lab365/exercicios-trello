// Utilize uma estrutura condicional (if,else) para verificar se um número é positivo, negativo ou zero. Exiba os valores no console.log()

let number = -3;
let answer = "";

if (number > 0) {
    answer = "Positivo"
} else if (number < 0) {
    answer = "Negativo";
} else {
    answer = "Zero";
}

console.log(answer);